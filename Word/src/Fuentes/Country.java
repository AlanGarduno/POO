package Fuentes;

/**
 *
 * @author JCVELMON
 */
public class Country 
{
    private String nombre;
    private String capital;

    public Country(String nombre, String capital) {
        this.nombre = nombre;
        this.capital = capital;
    }

    public String getNombre() {
        return nombre;
    }

    public String getCapital() {
        return capital;
    }
    
    
    
}
